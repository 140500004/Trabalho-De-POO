    private void jComboBoxGeneroMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jComboBoxGeneroMouseClicked
        // TODO add your handling code here:
        jComboBoxGenero.removeAllItems();
        ResultSet res;
        res = db.ListaGenero();
        if(res!=null) {
            try {
                while(res.next()) {
                    jComboBoxGenero.addItem(res.getString("Nome"));
                }
            } catch (SQLException e){
                JOptionPane.showMessageDialog(null, "Problema para exibir registros!");
            }
        } else {
            JOptionPane.showMessageDialog(null, "A pesquisa n√£o retornou nenhum registro!");
        }
    }//GEN-LAST:event_jComboBoxGeneroMouseClicked

