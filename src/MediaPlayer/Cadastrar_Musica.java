    private void jButtonEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEnviarActionPerformed
        // TODO add your handling code here:
        Nome = jTextFieldNome.getText();
        Ano =  Integer.valueOf(jTextFieldAno.getText());
        Tempo =  Float.valueOf(jTextFieldTempo.getText());
        Nota =  Integer.valueOf(jTextFieldNota.getText());
        Album =  jComboBoxMusica.getSelectedItem().toString();
        
        Nome = Nome.toUpperCase();
        Album = Album.toUpperCase();
        db.inserirMusica(Nome, Tempo, Ano, Nota, Album);
        
        jTextFieldNome.setText("");
        jTextFieldAno.setText("");
        jTextFieldTempo.setText("");
        jTextFieldNota.setText("");
        jComboBoxMusica.setSelectedItem(null);
    }//GEN-LAST:event_jButtonEnviarActionPerformed

