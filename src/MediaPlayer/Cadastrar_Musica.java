    private void jComboBoxMusicaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jComboBoxMusicaMouseClicked
        // TODO add your handling code here:
        jComboBoxMusica.removeAllItems();
        ResultSet res;
        res = db.ListaAlbum();
        if(res!=null) {
            try {
                while(res.next()) {
                    jComboBoxMusica.addItem(res.getString("Nome"));
                }
            } catch (SQLException e){
                JOptionPane.showMessageDialog(null, "Problema para exibir registros!");
            }
        } else {
            JOptionPane.showMessageDialog(null, "A pesquisa n√£o retornou nenhum registro!");
        }
    }//GEN-LAST:event_jComboBoxMusicaMouseClicked

